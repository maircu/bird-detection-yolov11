    '类别预测采用独立的Sigmoid激活而非Softmax，支持多标签分类。'
    'YOLOv3还引入了特征金字塔的上采样和拼接操作，将深层语义特征与浅层细节特征融合，'
    '提升了小目标的检测能力。YOLOv3在COCO数据集上的mAP50达到57.9%，'
