add_heading2(doc, '3.3 遮挡增强训练')

add_body(doc,
    '在自然场景中，鸟类常被树枝、树叶等遮挡物部分遮蔽，导致关键识别特征丢失，品种识别准确率显著下降。'
    '遮挡是细粒度目标识别中的经典难题，其挑战在于：遮挡导致目标的部分视觉信息不可用，'
    '模型需要仅凭可见部分的特征进行品种判别，这对模型的特征提取能力提出了更高的要求。'
    '在鸟类检测场景中，遮挡问题尤为突出，因为鸟类经常栖息在树枝上或灌丛中，'
    '被树枝和树叶遮挡是常态而非例外。遮挡比例从轻微的10%到严重的80%不等，'
    '随着遮挡比例的增加，品种识别的难度急剧上升。'
    '当遮挡超过50%时，大部分品种判别性特征被遮挡，模型只能依赖剩余的可见部分（如尾部、翅尖等）进行识别，'
    '品种识别准确率大幅下降。因此，提升模型在遮挡场景下的品种识别鲁棒性是本项目的重要目标之一。')

add_body(doc,
    '本项目开发了遮挡增强数据生成管线，通过在训练图像上人工合成遮挡效果，扩充遮挡场景的训练样本。'
    '该管线的输入为原始训练图像及其标注文件，输出为添加遮挡效果后的增强图像和保持不变的标注文件'
    '（遮挡不改变目标的类别和位置）。遮挡增强的核心步骤如下：')

add_body(doc,
    '第一步，目标区域定位。根据标注文件中的边界框信息，确定每个鸟类目标在图像中的位置和大小。'
    '遮挡物将叠加在目标区域上方，确保遮挡效果直接影响目标的可见性。'
    '目标区域定位的精确性对遮挡增强的效果至关重要：如果遮挡物偏离目标区域，'
    '则遮挡增强无效；如果遮挡物完全覆盖目标区域，则目标特征丢失过多导致品种无法识别。'
    '因此，本项目将遮挡物的中心位置约束在目标边界框内，并控制遮挡物的大小不超过目标面积的60%，'
    '确保遮挡效果既有效又合理。')

add_body(doc,
    '第二步，遮挡物生成。本项目实现了三种类型的遮挡物生成方法。'
    '树枝遮挡：从自然图像中提取树枝纹理，通过随机旋转、缩放和透明度调整后叠加到目标区域上方。'
    '树枝纹理的提取采用边缘检测和色彩分割的方法，从包含树枝的自然图像中分离出树枝区域，'
    '保存为带透明通道的PNG图像。树叶遮挡：生成半透明绿色色块模拟树叶遮挡效果，'
    '色块的形状为随机变形的椭圆，颜色在绿色范围内随机变化，透明度在0.3至0.7之间随机选取。'
    '色块遮挡：生成随机颜色的半透明矩形色块模拟各种遮挡物，色块的长宽比在0.5至2.0之间随机选取，'
    '颜色从自然场景的典型颜色（棕色、灰色、绿色等）中随机选取，透明度在0.3至0.7之间随机选取。'
    '三种遮挡类型的多样性确保了模型能够学习到不同遮挡条件下的品种判别能力，'
    '避免对特定遮挡类型的过拟合。')

add_body(doc,
    '第三步，遮挡比例控制。遮挡比例定义为遮挡物面积与目标面积的比值，'
    '本项目将遮挡比例控制在10%至60%之间。遮挡比例过低时增强效果不明显，'
    '过高时目标特征丢失过多导致品种无法识别。通过随机采样遮挡比例，'
    '模型能够学习到不同遮挡程度下的品种判别能力。遮挡比例的分布采用均匀分布，'
    '确保从轻微遮挡到重度遮挡的各种程度都能被充分覆盖。'
    '在实际生成过程中，遮挡比例通过调整遮挡物的大小来控制：'
    '遮挡物的大小为目标面积乘以遮挡比例，然后随机调整遮挡物的长宽比使其更自然。')

add_body(doc, '第四步，遮挡物叠加。将生成的遮挡物叠加到目标区域上方，叠加方式为Alpha混合：')
add_formula(doc, 'I_augmented = α × I_occluder + (1 - α) × I_original')

add_body(doc,
    '其中α为遮挡物的透明度，I_occluder为遮挡物像素值，I_original为原始像素值。'
    'Alpha混合确保了遮挡物与原始图像的自然融合，避免了生硬的边界。'
    '透明度α的取值范围设为0.3至0.7，较低的透明度使遮挡物半透明，'
    '目标的部分特征仍可透过遮挡物被观察到，模拟了真实的半透明遮挡效果（如薄树叶）；'
    '较高的透明度使遮挡物更为不透明，目标的特征被更严重地遮蔽，'
    '模拟了厚实的遮挡效果（如粗树枝）。')

add_body(doc,
    '通过该管线共生成9083张遮挡增强图像，占训练集总量的50%。'
    '遮挡增强训练后，品种识别模型在遮挡场景下的mAP50从0.824提升至0.856，提升幅度为3.2%。'
    '同时，本项目在推理阶段引入了裁剪二次验证机制，当品种模型对某个检测框的置信度不足时，'
    '将该区域裁剪并放大后重新输入品种模型进行识别。裁剪二次验证的原理在于：'
    '在全图推理时，远距离小鸟在特征图上仅占少量像素，特征信息可能被周围背景稀释；'
    '裁剪放大后，目标在输入图像中占据更大比例，特征信息更为集中，品种识别的准确率相应提升。'
    '实验表明，裁剪二次验证以零训练成本额外提升遮挡场景品种识别率约5%。'
    '裁剪二次验证的优势在于无需额外的训练数据和模型修改，仅在推理阶段增加一次前向计算，'
    '实现简单且效果显著。')

add_heading2(doc, '3.4 小目标剪影数据增强')

add_body(doc,
    '远距离小鸟在画面中仅占十余像素，且由于距离过远，色彩信息基本丢失，仅保留剪影形态。'
    '原始训练集中缺乏此类样本，导致模型对远距离小鸟的检测能力严重不足。'
    '在标准推理模式下，即使将置信度阈值降低至0.05，远距离小鸟仍难以被检出，'
    '因为模型从未在训练过程中见过如此小尺度的鸟类目标。'
    '小目标检测是目标检测领域的经典难题，其核心挑战在于：小目标在特征图上的表示极为有限，'
    '经过多次下采样后可能仅占不到一个像素，特征信息几乎完全丢失；'
    '小目标的训练样本在数据集中的占比很小，模型对小目标的梯度贡献容易被大目标淹没；'
    '小目标与背景的对比度低，容易被背景噪声干扰。'
    '上述挑战使得标准的目标检测模型对小目标的检测能力严重不足，'
    '需要通过针对性的数据增强和训练策略来改善。')

add_body(doc,
    '为解决这一问题，本项目开发了小目标剪影数据增强管线。该管线的核心思路是从训练集中裁剪出大尺寸的鸟类目标，'
    '通过缩小和剪影化处理模拟远距离小鸟的视觉效果，然后粘贴到典型背景上生成训练样本。具体步骤如下：')

add_body(doc,
    '第一步，大鸟裁剪。从训练集中选取边界框面积大于10000像素的鸟类目标，'
    '根据标注框坐标从原始图像中裁剪出鸟类区域。裁剪时保留一定的上下文信息，'
    '即在标注框基础上向外扩展15%的边距。共裁剪出约1500个大尺寸鸟类目标。'
    '大鸟裁剪的目的是获取高质量的鸟类图像，这些图像包含了丰富的鸟类细节特征，'
    '如羽色纹理、喙部形状、翅膀轮廓等，为后续的缩小和剪影化处理提供了优质的素材。'
    '裁剪时保留上下文信息的原因是：在实际场景中，鸟类目标周围通常有一定的背景区域，'
    '保留上下文信息使缩小后的目标与背景的过渡更加自然。')

add_body(doc, '第二步，尺度缩小。将裁剪出的大鸟图像缩小至15至50像素的范围内，模拟远距离小鸟的尺度。缩小比例根据原始尺寸和目标尺寸计算：')
add_formula(doc, 'scale = target_size / max(original_width, original_height)')

add_body(doc,
    '缩小后的图像通过双线性插值保持平滑的边缘。缩小比例的分布采用均匀分布，'
    '确保不同尺度的远距离小鸟都能被覆盖。尺度缩小的关键在于选择合适的目标尺寸范围：'
    '过大的目标尺寸（如100像素）不能有效模拟远距离小鸟，'
    '过小的目标尺寸（如5像素）在缩小过程中会丢失过多信息导致无法识别。'
    '15至50像素的范围是基于实际场景中远距离小鸟的典型尺度确定的，'
    '能够有效模拟从极远距离到中等距离的鸟类目标。')

add_body(doc,
    '第三步，剪影化处理。60%的缩小样本被进一步转换为暗色剪影形态。'
    '剪影化的具体方法为：将缩小后的鸟类图像转换为灰度图，通过Otsu阈值分割提取前景掩码，'
    '然后将前景像素的亮度值统一设置为暗色（RGB值在(20,20,20)至(60,60,60)之间随机选取），'
    '背景保持透明。剪影化模拟了逆光或远距离条件下的视觉效果，'
    '此时鸟类的色彩信息完全丢失，仅保留轮廓形状。'
    '剪影化比例设为60%的原因是：在实际场景中，远距离小鸟由于逆光或大气散射的影响，'
    '大部分呈现剪影形态，仅有少部分在顺光条件下保留色彩信息。'
    '60%的剪影化比例与实际场景中的比例基本一致，使训练数据更贴近真实分布。')

add_body(doc,
    '第四步，背景粘贴。将缩小后的鸟类目标（原始色彩或剪影形态）粘贴到天空、水面等典型背景上。'
    '背景图像从训练集中不含鸟类目标的区域中随机选取，确保背景的多样性和真实性。'
    '粘贴位置在背景图像上随机选取，但避免与已有目标重叠。'
    '粘贴时通过Alpha混合将鸟类目标与背景自然融合：')

add_formula(doc, 'I_result = α × I_bird + (1 - α) × I_background')

add_body(doc,
    '其中α为鸟类目标的不透明度，设为1.0以确保目标清晰可见。'
    '背景粘贴的关键在于选择合适的背景图像：背景应与实际检测场景一致，'
    '包括天空、水面、草地等典型环境，避免使用与实际场景差异过大的背景。'
    '粘贴位置的随机性确保了小目标在画面中的位置分布与实际场景一致，'
    '避免模型对特定位置的小目标产生偏好。')

add_body(doc,
    '第五步，标注生成。根据粘贴位置和缩小比例计算新的边界框坐标，生成对应的YOLO格式标签文件。'
    '标注格式为"class_id center_x center_y width height"，坐标值归一化至0至1范围。'
    '标注生成的精确性对小目标训练至关重要：即使是1至2像素的标注偏差，'
    '对于15像素的小目标而言也意味着约10%的定位误差，可能导致模型学习到错误的定位信息。'
    '因此，本项目在标注生成过程中采用了精确的坐标计算，确保标注与目标的实际位置完全一致。')

add_body(doc,
    '通过该管线共生成3000张小目标鸟图像。此外，该管线还生成了500张飞机、风筝、无人机等空中非鸟类目标的形状负样本，'
    '用于抑制空中目标的误检。形状负样本的生成方法与上述类似，'
    '区别在于粘贴的目标为飞机、风筝等非鸟类轮廓而非鸟类目标，且对应的标签文件为空文件（不包含任何目标）。'
    '小目标剪影数据增强的训练策略为：从一级检测模型bird_detect_v4的权重进行微调，'
    '训练参数为box=12.0（加大边界框损失权重）、cls=2.0（加大分类损失权重）、dfl=3.0（加大分布焦点损失权重），'
    '以强化模型对小目标的学习。增大损失权重的原理在于：小目标在训练集中的占比很小，'
    '标准损失权重下小目标的梯度贡献容易被大目标的梯度淹没，'
    '通过增大损失权重使模型更加关注小目标的检测质量。')

add_heading2(doc, '3.5 推理阶段误检抑制')

add_body(doc,
    '在不重新训练模型的前提下，本项目在推理阶段设计了多层误检抑制机制，有效降低了建筑物等非鸟类目标的误检率。'
    '推理阶段误检抑制的优势在于无需收集负样本和重新训练模型，可以快速部署和调整，'
    '适合对已部署模型的快速优化。在实际应用中，模型部署后发现的误检问题往往需要快速解决，'
    '而重新收集数据、训练模型的周期较长，难以满足快速迭代的需求。'
    '推理阶段的误检抑制方法通过后处理规则对检测结果进行过滤，'
    '无需修改模型权重，具有部署简单、调整灵活的优势。')

add_body(doc,
    '第一层：宽高比与面积过滤。鸟类目标的宽高比通常集中在0.25至4.0之间，'
    '且单只鸟在画面中的面积占比一般不超过25%。超出此范围的检测框大概率不是鸟类目标，可以直接过滤。'
    '具体而言，对于每个检测框(x1, y1, x2, y2)，计算其宽度bw=x2-x1、高度bh=y2-y1和宽高比aspect=bw/bh。'
    '如果aspect<0.25或aspect>4.0，则该框被过滤；如果bw×bh/(H×W)>0.25（H和W为画面高度和宽度），'
    '则该框也被过滤。宽高比过滤的逻辑在于：建筑物的尖顶结构通常产生极端窄长的检测框（aspect<0.25），'
    '建筑物的横梁结构通常产生极端扁平的检测框（aspect>4.0），'
    '而鸟类无论是站立、飞行还是蹲伏，其宽高比都在较为合理的范围内。'
    '面积过滤的逻辑在于：单只鸟在画面中的面积占比不可能超过25%，'
    '超过此比例的检测框大概率是建筑物、墙壁等大面积非鸟类目标。'
    '宽高比与面积过滤是最简单高效的误检抑制方法，计算开销几乎为零，'
    '但仅能过滤明显不符合鸟类形态的检测框，对于宽高比和面积在合理范围内的误检（如建筑物尖顶）无效。')

add_body(doc,
    '第二层：时序一致性检查。在视频检测中，建筑物的位置在连续帧中保持固定不动，'
    '而鸟类会随时间移动（飞行、跳跃、转头等）。基于这一观察，'
    '本项目通过跟踪检测框在连续帧中的位置变化来识别静态物体。'
    '具体实现为：对每个检测框的位置进行量化（容差15像素），生成位置键值(key_x1, key_y1, key_x2, key_y2)，'
    '其中key_i = int(coordinate_i) // 15。在每一帧中，统计每个位置键值出现的连续帧数。'
    '当某个位置键值的连续出现帧数超过阈值（本项目设为5帧）时，该位置被标记为静态物体，'
    '后续帧中出现在该位置的检测框将被过滤。位置量化的容差设置为15像素，'
    '以容忍视频压缩、相机抖动等导致的微小位置偏移。'
    '时序一致性检查的优势在于：它不需要任何关于建筑物外观的先验知识，'
    '仅利用运动信息即可识别静态物体，具有很好的通用性。'
    '但该方法仅适用于视频检测场景，对于单帧图像检测无效。'
    '此外，时序一致性检查可能误过滤长时间停留在同一位置的鸟类（如栖息在树枝上不动的鸟），'
    '这是该方法的一个已知局限性，后续可以通过引入运动检测模块来区分静止鸟类和建筑物。')

add_body(doc,
    '第三层：品种模型否决。当一级鸟检测模型检出某个目标但二级品种模型在该位置完全未检出任何鸟类品种时，'
    '说明品种模型对该区域是否为鸟类持否定态度。品种模型否决的逻辑在于：'
    '二级品种模型在33类鸟类数据上训练，对鸟类目标具有更强的判别能力，'
    '如果品种模型在全图推理中未能在该位置检出任何品种，大概率该目标不是鸟类。'
    '具体实现为：对于一级模型的每个检测框B_i，计算其与二级模型所有品种框的IoU值，'
    '如果最大IoU低于0.15，则触发品种模型否决。'
    '对于非切片模式，品种模型否决直接过滤该检测框；'
    '对于切片模式，由于低阈值检出的小目标可能在全图品种推理中被遗漏，'
    '因此给予一次裁剪二次验证的机会，如果裁剪验证也失败则过滤。'
    '裁剪二次验证失败的逻辑在于：裁剪放大后品种模型仍无法识别该目标为任何鸟类品种，'
    '进一步确认该目标不是鸟类。品种模型否决的优势在于利用了级联架构中二级品种模型的判别能力，'
    '相当于引入了额外的验证机制，提高了检测结果的可靠性。')

add_body(doc,
    '上述三层过滤机制互不冲突、叠加生效。宽高比与面积过滤在检测框生成后立即执行，效率最高；'
    '时序一致性检查在宽高比过滤后执行，利用历史帧信息；'
    '品种模型否决在与品种模型匹配后执行，利用品种模型的判别能力。'
    '三层过滤的执行顺序经过精心设计，确保计算效率最大化：'
    '先执行计算开销最小的规则过滤，再执行需要历史信息的时序检查，'
    '最后执行需要品种模型结果的否决判断。'
    '三层过滤的叠加效果显著：宽高比与面积过滤可以快速排除明显不合理的检测框，'
    '减少后续过滤的计算量；时序一致性检查可以识别建筑物等固定目标的误检，'
    '弥补了宽高比过滤无法处理的误检类型；品种模型否决可以作为最终的验证机制，'
    '过滤前两层未能排除的误检。三层过滤的组合使系统的误检率大幅下降，'
    '同时保持了较高的检测召回率，因为每层过滤都设置了合理的阈值，'
    '避免过度过滤导致真阳性被误排除。')

add_heading2(doc, '3.6 切片推理速度优化')

add_body(doc,
    '原始切片推理方案的处理速度仅约3fps，难以满足视频实时检测的需求。'
    '性能瓶颈主要来自以下几个方面：每个切片单独进行一次GPU推理调用，'
    '6至7次串行推理的累计延迟较高；全图预检测增加了1次额外的推理开销；'
    '切片推理分辨率设为1280，而切片本身仅有480至640像素，1280推理尺寸导致过度放大和计算浪费；'
    '25%的重叠率产生了过多的冗余切片。上述瓶颈的叠加效应使得切片推理的速度远低于全图推理，'
    '限制了切片推理在实际应用中的可用性。因此，对切片推理进行速度优化是本项目的重要工作之一。')

add_body(doc,
    '本项目从多个维度进行了速度优化。第一，批量推理。将所有切片合并为一次批量推理调用，'
    '替代原有的逐切片串行推理。Ultralytics框架的predict方法支持输入图像列表，'
    '内部会将列表中的所有图像组成一个batch进行并行推理，充分利用GPU的并行计算能力。'
    '批量推理减少了数据从CPU到GPU的传输次数和模型加载的开销，是速度提升的最主要来源。'
    '在串行推理模式下，每个切片需要单独进行一次CPU到GPU的数据传输和模型推理，'
    '数据传输的延迟在总推理时间中占比较大；批量推理将所有切片合并为一次数据传输和推理，'
    '大幅减少了数据传输的次数和总延迟。实验表明，批量推理相比串行推理速度提升约2倍。')

add_body(doc,
    '第二，去除全图预检测。原始方案在切片检测前先进行一次全图检测以保留大目标，'
    '但切片本身已覆盖全图，大目标在切片中同样能被检出。去除全图预检测减少了1次推理开销，'
    '且不影响检测完整性。全图预检测的原始设计意图是在切片检测前先检出大目标，'
    '避免大目标被切片截断导致检测失败。然而，在实际测试中发现，'
    '切片大小为640像素时，大部分大目标在切片中仍能被完整检出，'
    '因为640像素的切片已经足够容纳大部分鸟类目标。仅当目标尺寸超过640像素时，'
    '才可能被切片截断，但这种情况在实际场景中极为罕见。因此，去除全图预检测是合理的优化。')

add_body(doc,
    '第三，切片推理分辨率优化。切片大小为640像素，推理分辨率设为640即可保持1:1的像素对应关系，'
    '无需放大至1280。降低推理分辨率使每次推理的计算量减少至原来的约1/4'
    '（计算量与分辨率的平方成正比），对速度提升贡献显著。'
    '推理分辨率从1280降至640的精度影响极小，因为切片本身仅有480至640像素，'
    '使用1280推理尺寸相当于将切片放大2倍，这种过度放大不会带来额外的信息增益，'
    '反而会引入插值伪影和计算浪费。实验表明，640推理分辨率的检测精度与1280推理分辨率几乎一致，'
    '但推理速度提升约4倍。')

add_body(doc,
    '第四，切片参数调整。将切片大小从480提升至640，重叠率从25%降低至15%。'
    '切片大小增大后，每个切片覆盖的图像区域更大，所需切片数量减少。'
    '对于1280×720的视频，480切片25%重叠需要6至7个切片，640切片15%重叠仅需4个切片，'
    '减少了约40%的推理次数。重叠率从25%降低至15%在减少冗余的同时，'
    '仍能保证切片边界的检测覆盖，因为位于切片边界的目标会在相邻切片的重叠区域中被检出。'
    '15%的重叠率意味着相邻切片有约96像素的重叠区域，足以覆盖大部分鸟类目标，'
    '确保位于切片边界的目标不会被遗漏。')

add_body(doc,
    '经过上述优化，切片模式的处理速度从约3fps提升至约8至10fps，速度提升约3倍。'
    '在精度方面，由于切片大小增大和重叠率降低，位于切片边界的小目标可能被切分到两个切片中，'
    '每个切片中只包含目标的一部分，可能影响检测效果。但在实际测试中，这种影响可以忽略不计，'
    '因为15%的重叠率已经提供了足够的边界覆盖，且批量推理的精度与串行推理一致。'
    '速度优化的结果表明，通过合理的参数调整和计算优化，'
    '切片推理可以在保持检测精度的前提下显著提升处理速度，'
    '使其在实时性要求较高的应用场景中具备可用性。'
    '后续还可以通过动态切片策略（仅对天空区域切片）和模型蒸馏压缩进一步提升推理速度。')

