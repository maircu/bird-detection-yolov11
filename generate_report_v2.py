add_heading2(doc, '2.3 Inner-IoU损失函数')

add_body(doc,
    '边界框回归损失函数是目标检测模型训练中的关键组成部分，其设计直接影响模型的定位精度。'
    'IoU（Intersection over Union）是最基础的边界框评价指标，其计算公式为：')

add_formula(doc, 'IoU = |A ∩ B| / |A ∪ B|')

add_body(doc,
    '其中A为预测框区域，B为真实框区域，|A ∩ B|为两者的交集面积，|A ∪ B|为两者的并集面积。'
    'IoU的取值范围为[0,1]，值越大表示两个框的重叠程度越高。'
    '基于IoU的损失函数定义为L_IoU = 1 - IoU，该损失函数在IoU接近1时梯度趋近于0，'
    '导致模型在训练后期难以进一步优化定位精度。IoU损失的另一个问题是当两个框不重叠时，'
    'IoU值为0，梯度也为0，模型无法获得任何优化方向的信息。'
    '这两个问题严重限制了IoU损失在目标检测训练中的效果，'
    '促使研究者提出了多种改进的边界框回归损失函数。')

add_body(doc,
    '为解决IoU损失在训练后期的梯度消失问题，GIoU引入了最小包围矩形的概念，其计算公式为：')

add_formula(doc, 'GIoU = IoU - |C - A ∪ B| / |C|')

add_body(doc,
    '其中C为预测框和真实框的最小包围矩形。GIoU在IoU的基础上增加了对非重叠区域的惩罚，'
    '即使两个框不重叠也能提供有效的梯度信号。GIoU的取值范围为[-1,1]，'
    '当两个框完全重合时GIoU=1，当两个框距离无穷远时GIoU趋近于-1。'
    '然而，GIoU在两个框包含关系时退化为IoU，仍存在优化困难的问题。'
    '具体而言，当预测框完全包含真实框或真实框完全包含预测框时，'
    '最小包围矩形等于较大的那个框，非重叠区域惩罚项为0，GIoU退化为IoU。'
    '在这种情况下，模型只能依赖IoU的梯度进行优化，而IoU在重叠度较高时梯度微弱，'
    '导致收敛速度变慢。')

add_body(doc,
    'DIoU在GIoU的基础上进一步引入了中心点距离惩罚项，其计算公式为：')

add_formula(doc, 'DIoU = IoU - ρ²(b, b^gt) / c²')

add_body(doc,
    '其中ρ(b, b^gt)为预测框中心点与真实框中心点之间的欧氏距离，c为最小包围矩形的对角线长度。'
    'DIoU直接优化两个框的中心点距离，收敛速度显著快于GIoU。'
    'DIoU的优势在于：即使两个框不重叠，只要中心点距离在缩小，DIoU就能提供有效的梯度信号，'
    '驱动模型将预测框向真实框方向移动。DIoU的收敛速度比GIoU快约2至3倍，'
    '在COCO数据集上的实验表明，使用DIoU训练的模型在相同epoch数下能达到更高的mAP。'
    '然而，DIoU仅考虑了中心点距离，未考虑宽高比的一致性，'
    '当两个框的中心点重合但宽高比不同时，DIoU无法提供有效的优化方向。')

add_body(doc,
    'CIoU在DIoU的基础上增加了宽高比一致性惩罚项，其计算公式已在2.1节中给出。'
    'CIoU是目前YOLO系列默认使用的边界框回归损失，在大多数目标检测任务上表现良好。'
    'CIoU综合考虑了重叠面积、中心点距离和宽高比三个因素，'
    '提供了最全面的边界框回归监督信号。然而，在小目标检测场景中，CIoU仍存在以下不足：'
    '当目标像素占比很低时，即使预测框与真实框存在数像素的偏差，IoU值的变化也极为微小，'
    '导致梯度信号不足以驱动模型进行精细化的定位优化。'
    '例如，对于一个10×10像素的小目标，预测框偏移2像素时IoU仅从1.0降至约0.64，'
    '而对于一个100×100像素的大目标，同样的2像素偏移仅使IoU从1.0降至约0.96。'
    '这种差异导致小目标的定位优化梯度远弱于大目标，模型倾向于优先优化大目标的定位精度，'
    '而忽视小目标的定位优化。')

add_body(doc,
    'Inner-IoU损失函数针对小目标定位精度不足的问题进行了专门优化，其核心思想包含两个关键机制。'
    '第一，辅助内缩框机制。给定预测框P = (x1, y1, x2, y2)和真实框G = (x1^gt, y1^gt, x2^gt, y2^gt)，'
    'Inner-IoU首先计算两者的内缩框。内缩框的生成方式为：以原始框的中心点为基准，'
    '将框的宽度和高度分别乘以内缩比例ratio（本项目取0.7），生成面积更小的辅助框。具体计算公式为：')

add_formula(doc, 'P_inner = (cx + ratio×(x1-cx), cy + ratio×(y1-cy), cx + ratio×(x2-cx), cy + ratio×(y2-cy))')
add_formula(doc, 'G_inner = (cx^gt + ratio×(x1^gt-cx^gt), cy^gt + ratio×(y1^gt-cy^gt), cx^gt + ratio×(x2^gt-cx^gt), cy^gt + ratio×(y2^gt-cy^gt))')

add_body(doc,
    '其中(cx, cy)和(cx^gt, cy^gt)分别为预测框和真实框的中心点坐标。当ratio=0.7时，'
    '内缩框的面积约为原始框的49%（0.7²=0.49），由于面积更小，相同像素偏差导致的IoU变化更为显著，'
    '从而提供了更敏感的定位监督信号。内缩框的设计直觉是：对于小目标而言，'
    '原始IoU对定位偏差不够敏感，因为小目标的面积本身就很小，即使预测框与真实框存在数像素的偏差，'
    'IoU值的变化也不明显。通过缩小评估区域，内缩框放大了定位偏差的影响，'
    '使模型能够获得更强的梯度信号来优化小目标的定位精度。')

add_body(doc,
    '第二，焦点加权机制。在训练过程中，不同质量的预测框对模型优化的贡献应当不同：'
    '定位偏差较大的低质量预测框需要更强的梯度信号加速收敛，'
    '定位精度较高的高质量预测框则需要更温和的优化避免过拟合。'
    'Inner-IoU通过焦点加权机制实现这一目标，其计算公式为：')

add_formula(doc, 'w = Inner-IoU, 如果 IoU < 0.5')
add_formula(doc, 'w = 1, 如果 IoU ≥ 0.5')

add_body(doc, '最终损失计算公式为：')
add_formula(doc, 'L_Inner-IoU = CIoU - d × w')

add_body(doc,
    '其中d为惩罚系数，w为焦点权重。当IoU低于0.5时，焦点权重等于Inner-IoU值，'
    '由于Inner-IoU通常小于IoU，此时损失值增大，相当于对低质量预测施加了额外的惩罚；'
    '当IoU高于0.5时，焦点权重为1，损失退化为标准CIoU损失，不额外施加惩罚。'
    '焦点加权机制的设计理念与Focal Loss类似：在训练初期，大部分预测框的质量较低（IoU < 0.5），'
    '焦点加权增强了这些预测框的梯度贡献，加速模型收敛；在训练后期，大部分预测框的质量已较高（IoU ≥ 0.5），'
    '焦点加权退化为标准CIoU，避免过度优化导致过拟合。这种动态调整策略使训练过程更加高效和稳定。')

add_body(doc,
    'Inner-IoU的设计优势在于：首先，辅助内缩框提供了比原始IoU更敏感的定位评价指标，'
    '特别适合小目标场景下精细化的定位优化；其次，焦点加权机制使模型在训练初期能够获得更强的梯度信号加速收敛，'
    '在训练后期退化为标准CIoU损失避免过度优化，实现了训练过程的动态调整；'
    '第三，Inner-IoU可以无缝替换CIoU作为边界框回归损失，不需要修改模型架构或训练流程，实现简单。'
    '在本项目的实验中，Inner-IoU使小目标定位精度mAP50-95提升了约3%，'
    '验证了其在小目标检测场景中的有效性。值得注意的是，Inner-IoU对mAP50的提升不大（仅0.2%），'
    '这是因为mAP50仅在IoU=0.50阈值下评估检测性能，对定位精度的敏感度较低；'
    '而mAP50-95在IoU从0.50到0.95的多个阈值下评估检测性能，对定位精度的敏感度更高，'
    '因此Inner-IoU的定位优化效果在mAP50-95上体现得更为明显。'
    '这表明Inner-IoU的主要贡献在于提升小目标的定位精度，而非检测召回率。')

add_image_placeholder(doc, '【图2-3：Inner-IoU内缩框示意图，来源：ppt_diagrams.html中PPT1中Inner-IoU模块详解】')

add_heading2(doc, '2.4 双模型级联架构')

add_body(doc,
    '在传统的单模型目标检测方案中，模型需要同时完成"画面中是否存在目标"的检测任务和"目标属于哪个类别"的分类任务。'
    '对于鸟类检测这一特定应用场景，检测任务要求模型对所有鸟类目标保持高召回率，'
    '即不遗漏画面中的任何一只鸟，这倾向于宽松的分类边界；而品种识别任务要求模型在细粒度层面精确区分33类相似品种，'
    '即正确判断每只鸟的具体种类，这倾向于严格的分类边界。两个任务的优化方向存在内在冲突：'
    '宽松的分类边界有助于提升检测召回率但会降低品种识别精度，'
    '严格的分类边界有助于提升品种识别精度但可能导致部分鸟类目标被遗漏。'
    '这种优化冲突在细粒度目标检测任务中普遍存在，是单模型方案难以克服的根本性矛盾。')

add_body(doc,
    '为解决这一优化矛盾，本项目提出了双模型级联架构，将检测与识别两个任务解耦到独立的模型中。'
    '一级模型（bird_detect_v4）仅负责检测画面中的鸟类目标，输出单类"鸟"的边界框和置信度。'
    '由于一级模型只需判断"是否为鸟"这一二分类问题，训练时不受品种类别间细微差异的干扰，'
    '能够专注优化检测召回率。二级模型（species_occlusion_v2）仅对一级模型检出的鸟区域进行品种判定，'
    '输出33类具体品种名称和置信度。由于二级模型只需在已确认存在鸟类的区域内进行品种分类，'
    '训练时专注于细粒度分类精度的提升，不受背景干扰的影响。'
    '双模型级联的设计灵感来源于人类专家的识别过程：首先快速扫描画面判断是否存在鸟类，'
    '然后对发现的鸟类仔细观察确定品种。这种分阶段的处理方式使每个阶段都能专注于单一任务，'
    '避免了多任务之间的相互干扰。')

add_body(doc,
    '两个模型在推理阶段通过IoU匹配进行关联。具体而言，对于一级模型输出的每个检测框B_i，'
    '计算其与二级模型输出的所有品种框S_j的IoU值，选择IoU最大的品种框作为匹配结果：')

add_formula(doc, 'best_j = argmax_j IoU(B_i, S_j)')

add_body(doc,
    '当最大IoU值超过匹配阈值（本项目设为0.20）时，认为两个框指向同一只鸟，'
    '将品种框的类别和置信度赋给检测框。匹配阈值设为0.20的原因在于：'
    '一级模型和二级模型的检测框可能存在一定偏差，尤其是当目标较小或被遮挡时，'
    '两个模型对目标边界的判断可能不一致，0.20的阈值能够容忍这种偏差同时避免错误匹配。'
    '当最大IoU值低于匹配阈值时，说明二级模型未在该位置检出任何品种，'
    '此时根据检测框的置信度决定后续处理策略：如果置信度较高，标注为通用"鸟"标签；'
    '如果置信度较低且通过品种模型否决检查，则过滤该检测框。')

add_body(doc,
    '在级联架构的推理过程中，可能出现同一只鸟同时被标出品种名和通用"鸟"标签的重复标注问题。'
    '这是由于一级模型和二级模型分别独立检测，当两者的检测框存在部分重叠但IoU未达到NMS阈值时，'
    '两个检测结果都会被保留。为解决这一问题，本项目设计了Cascade-NMS品种优先非极大值抑制算法，'
    '其核心逻辑为：当两个检测框的IoU超过阈值（本项目设为0.35）时，品种标签优先于通用"鸟"标签。'
    '具体而言，如果两个重叠框中一个是具体品种名（如"白鹭"），另一个是通用"鸟"标签，'
    '则保留品种标签框、丢弃"鸟"标签框；如果两个框都是品种标签或都是"鸟"标签，'
    '则保留置信度较高的框、丢弃置信度较低的框。'
    '这一设计确保了每只鸟最终只被标注一个标签，且品种标签优先于通用标签，避免了信息冗余。')

add_body(doc,
    'Cascade-NMS的算法流程如下。首先，将所有检测框按置信度降序排列。'
    '然后，依次遍历每个检测框，对于当前框B_i，检查其与所有已保留框B_j的IoU值。'
    '如果IoU超过阈值0.35，则根据标签优先级决定保留策略：'
    '若B_i的标签为"鸟"而B_j的标签为品种名，则丢弃B_i；'
    '若B_i的标签为品种名而B_j的标签为"鸟"，则从已保留列表中移除B_j并保留B_i；'
    '若两者标签类型相同，则丢弃置信度较低的框。'
    '该算法的时间复杂度为O(n²)，其中n为检测框数量，'
    '在鸟类检测场景中n通常较小（每帧不超过数十个检测框），计算开销可忽略不计。'
    'Cascade-NMS与标准NMS的关键区别在于引入了标签优先级的概念：'
    '标准NMS仅根据置信度决定保留策略，不考虑标签的语义差异；'
    'Cascade-NMS在置信度的基础上增加了标签类型的判断，使品种标签具有更高的优先级，'
    '确保了系统输出中品种信息的完整性和准确性。')

add_body(doc,
    '双模型级联架构的优势体现在以下几个方面。第一，解耦优化。'
    '两个模型可以独立选择最优的训练策略、数据增强方式和超参数，不受彼此约束。'
    '一级模型采用负样本训练抑制误检，二级模型采用遮挡增强训练提升品种识别鲁棒性，各自发挥最大效能。'
    '一级模型使用640分辨率训练即可满足检测需求，而二级模型需要1280分辨率才能获取足够的细节信息进行品种识别，'
    '两个模型对输入分辨率的不同需求在级联架构中可以灵活满足。'
    '第二，灵活扩展。当需要增加新品种时，只需重新训练二级品种识别模型，一级检测模型无需改动；'
    '当需要调整检测灵敏度时，只需调整一级模型的置信度阈值，二级品种识别模型无需改动。'
    '这种模块化的设计使得系统的维护和升级成本大大降低。'
    '第三，容错机制。通过置信度分级输出规则，高于阈值输出具体品种名，低于阈值标注为通用"鸟"标签，'
    '确保系统不会将非鸟类目标误识别为特定品种，在保证安全性的前提下最大化信息输出。'
    '这种分级输出策略在安全关键型应用中尤为重要，因为它确保了系统不会产生高置信度的错误品种判断，'
    '而是将不确定的情况降级为通用标签，由人工进一步确认。')

add_image_placeholder(doc, '【图2-4：双模型级联架构示意图，来源：ppt_diagrams.html中PPT1中双模型级联模块详解】')

add_heading2(doc, '2.5 SAHI切片辅助推理')

add_body(doc,
    'SAHI（Slicing Aided Hyper Inference）是一种面向大尺寸图像中小目标检测的推理策略，'
    '由Akyon等人于2022年提出。其核心思想是在推理阶段将输入图像切分为多个重叠的小块，'
    '分别对每个小块进行目标检测，再将所有切片的检测结果映射回原图坐标系并合并，'
    '最终通过非极大值抑制去除重叠区域的重复检测。'
    'SAHI的提出填补了目标检测领域在推理阶段处理小目标的方法空白，'
    '为不修改模型架构即可提升小目标检测能力提供了一种简单有效的解决方案。')

add_body(doc,
    'SAHI的设计动机源于以下观察：在标准的目标检测流程中，输入图像会被缩放到固定的推理分辨率（如640×640或1280×1280），'
    '对于高分辨率的输入图像，缩放操作会导致远距离小目标的像素信息进一步压缩，'
    '使其在特征图上仅占极少的像素甚至不到一个像素，从而无法被模型有效检测。'
    '切片推理通过将大图分割为小块，使每个小块在缩放到推理分辨率时保留了更多的局部细节，'
    '相当于为模型提供了一个"放大镜"，使原本在全局图像中仅占十余像素的远距离小鸟在切片中占据更多像素，'
    '从而被模型有效检出。SAHI的切片策略本质上是一种空间维度的数据增强，'
    '通过改变输入图像的观察尺度来提升模型对不同大小目标的检测能力。')

add_body(doc,
    'SAHI的切片策略如下。给定输入图像I ∈ R^(H×W×3)，切片大小为S，重叠率为O，则切片步长为：')

add_formula(doc, 'step = S × (1 - O)')

add_body(doc, '对于图像的每个位置(y_start, x_start)，提取切片：')
add_formula(doc, 'I_slice = I[y_start : y_start+S, x_start : x_start+S]')
add_body(doc, '切片的起始坐标为：')
