add_heading2(doc, '2.2 EMA多尺度注意力模块')

add_body(doc,
    '注意力机制是深度学习中用于增强模型对关键信息关注能力的重要技术手段。'
    '在目标检测领域，注意力机制的核心目标是通过学习到的权重对特征图进行自适应调整，'
    '使模型更加关注与目标相关的区域，同时抑制背景噪声的干扰。'
    '根据注意力作用范围的不同，可以将注意力机制分为通道注意力、空间注意力和自注意力三大类。'
    '通道注意力（如SE模块）通过对特征图的通道维度进行全局池化和全连接变换，'
    '生成通道级别的注意力权重，使模型能够选择性地增强重要通道的特征表达。'
    'SE模块由Hu等人于2018年提出，其核心操作包括全局平均池化、全连接降维、ReLU激活、'
    '全连接升维和Sigmoid激活，最终生成通道注意力权重与原始特征图逐通道相乘。'
    'SE模块的计算复杂度极低，仅增加不到1%的参数量，但能带来显著的性能提升，'
    '被广泛应用于各种视觉任务的主干网络中。')

add_body(doc,
    '空间注意力（如CBAM的空间注意力模块）通过对特征图的空间维度进行聚合，'
    '生成空间位置的注意力权重，使模型能够聚焦于目标所在的空间区域。'
    'CBAM（Convolutional Block Attention Module）由Woo等人于2018年提出，'
    '将通道注意力和空间注意力串联组合，先通过通道注意力增强重要通道的特征，'
    '再通过空间注意力聚焦于重要的空间位置。空间注意力模块通过对特征图沿通道维度进行最大池化和平均池化，'
    '将两个池化结果拼接后通过卷积生成空间注意力权重。'
    'CBAM的设计理念是通道注意力和空间注意力的互补：通道注意力回答"看什么特征"，'
    '空间注意力回答"看哪个位置"，两者结合提供了更全面的注意力增强。')

add_body(doc,
    '自注意力机制（如Transformer）通过计算特征图中任意两个位置之间的相似度，'
    '建立全局依赖关系，具有最强的建模能力，但计算复杂度为O(n²)，'
    '对于高分辨率特征图而言计算开销过大。Transformer最初由Vaswani等人于2017年提出用于自然语言处理，'
    '随后被引入计算机视觉领域。Vision Transformer（ViT）将图像切分为固定大小的patch，'
    '通过自注意力机制建立patch之间的全局依赖关系，在大规模数据集上取得了优异的性能。'
    '然而，自注意力机制的计算复杂度与序列长度的平方成正比，'
    '对于高分辨率特征图（如640×640的特征图序列长度为1600），计算开销极为巨大。'
    '为降低计算复杂度，研究者提出了多种高效的注意力变体，'
    '如窗口注意力（Swin Transformer）、线性注意力（Linear Transformer）和分组注意力等，'
    '在保持注意力增强效果的同时大幅降低了计算开销。')

add_body(doc,
    'EMA（Efficient Multi-Scale Attention）是一种高效的轻量级注意力机制，'
    '其设计目标是在不显著增加计算量的前提下增强模型对多尺度目标的感知能力。'
    '与SE模块仅关注通道注意力不同，EMA同时建模了空间维度上的方向性依赖关系；'
    '与自注意力机制不同，EMA通过分组1D池化替代全局自注意力计算，'
    '将计算复杂度从O(n²)降低至O(n)，在保持高效的同时实现了多尺度注意力增强。'
    'EMA模块的设计灵感来源于对目标检测中空间注意力需求的观察：'
    '在目标检测任务中，不同方向上的空间依赖关系具有不同的重要性，'
    '例如水平方向上的依赖关系有助于检测水平伸展的翅膀，'
    '垂直方向上的依赖关系有助于检测垂直延伸的尾羽，'
    '而全局自注意力虽然能够建模所有方向上的依赖关系，但计算代价过高。'
    'EMA通过分别沿X方向和Y方向进行1D池化，高效地捕捉了两个正交方向上的空间依赖关系，'
    '再通过跨空间融合将两者结合，实现了方向感知的多尺度注意力增强。')

add_body(doc,
    'EMA模块的具体计算过程如下。给定输入特征图X ∈ R^(C×H×W)，其中C为通道数，H为高度，W为宽度。'
    '首先，将输入特征图沿X方向进行1D平均池化，即对每一行取平均值，生成X方向的池化特征：')

add_formula(doc, 'X_x = AvgPool_x(X) ∈ R^(C×H×1)')

add_body(doc,
    '其中AvgPool_x表示沿宽度维度W的平均池化操作。该操作将每一行的W个值取平均，'
    '生成一个H×1的特征向量，编码了X方向上的全局信息。'
    '类似地，沿Y方向进行1D平均池化，即对每一列取平均值：')

add_formula(doc, 'X_y = AvgPool_y(X) ∈ R^(C×1×W)')

add_body(doc,
    '其中AvgPool_y表示沿高度维度H的平均池化操作。该操作将每一列的H个值取平均，'
    '生成一个1×W的特征向量，编码了Y方向上的全局信息。'
    '随后，分别对两组池化特征通过Softmax函数生成注意力权重：')

add_formula(doc, 'A_x = Softmax(Conv1x1(X_x)) ∈ R^(C×H×1)')
add_formula(doc, 'A_y = Softmax(Conv1x1(X_y)) ∈ R^(C×1×W)')

add_body(doc,
    '其中Conv1x1为1×1卷积层，用于调整通道维度和引入非线性变换。'
    'Softmax函数确保注意力权重在各自维度上归一化，其计算公式为：')

add_formula(doc, 'Softmax(z_i) = exp(z_i) / Σ_j exp(z_j)')

add_body(doc,
    '接下来，通过跨空间特征融合机制将两组方向性注意力权重进行交互融合。'
    '具体而言，将X方向注意力权重A_x沿宽度维度广播至与输入特征图相同的形状，'
    '将Y方向注意力权重A_y沿高度维度广播至与输入特征图相同的形状，'
    '然后进行逐元素相乘实现跨空间融合：')

add_formula(doc, 'A_fused = A_x ⊗ A_y ∈ R^(C×H×W)')

add_body(doc,
    '其中⊗表示逐元素乘法。融合后的注意力权重A_fused同时编码了X方向和Y方向的位置信息，'
    '能够捕捉不同方向上的长距离依赖关系。跨空间融合的设计理念是：'
    'X方向注意力权重编码了每一行的全局信息，Y方向注意力权重编码了每一列的全局信息，'
    '两者的逐元素乘积将行列信息进行交互，生成每个空间位置的联合注意力权重。'
    '这种融合方式比简单的加法融合更有效，因为乘法能够捕捉行列信息之间的交互效应，'
    '而加法仅能独立地组合行列信息。最后，通过残差连接将注意力加权后的特征与原始输入相加：')

add_formula(doc, 'Y = X ⊗ A_fused + X')

add_body(doc,
    '残差连接确保了梯度稳定传播的同时引入注意力增强，避免了注意力模块导致的梯度消失问题。'
    '残差连接的设计是深度学习中常用的技巧，其核心思想是让注意力模块学习对原始特征的增量修正，'
    '而非从零开始学习新的特征表示。当注意力权重全为1时，输出等于输入，模块退化为恒等映射；'
    '当注意力权重偏离1时，模块对原始特征进行增强或抑制。这种设计确保了注意力模块的引入'
    '不会降低模型的基线性能，只会在此基础上进行改进。')

add_body(doc,
    'EMA模块的优势体现在以下几个方面。第一，计算效率高。EMA通过1D池化替代2D全局池化或自注意力计算，'
    '计算复杂度从O(H×W)或O(H²×W²)降低至O(H+W)，对于高分辨率特征图而言计算开销极小。'
    '第二，方向感知能力强。分别沿X方向和Y方向建模注意力，能够有效捕捉水平伸展的翅膀和垂直延伸的尾羽等鸟类特征，'
    '这与鸟类检测任务中目标的方向性特征高度契合。'
    '第三，多尺度适应性好。EMA模块可以无缝嵌入到特征金字塔的不同层级中，'
    '在浅层特征图上关注细节信息，在深层特征图上关注语义信息，实现多尺度的注意力增强。'
    '在本项目的消融实验中，EMA模块使小目标检测mAP从0.490提升至0.785，提升幅度达60.9%，'
    '充分证明了其在多尺度目标检测中的有效性。'
    'EMA模块的参数量仅增加不到0.1M，推理速度几乎不受影响，'
    '在精度和效率之间实现了极佳的平衡。')

